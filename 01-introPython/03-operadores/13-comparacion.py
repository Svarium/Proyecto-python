#OPERADORES DE COMPARACION

#no es lo mismo usar el = para asignar que dos == para comparar
# == para comparar igualdad
# != para comparar desigualdad
# > para comparar si es mayor
# < para comparar si es menor
# >= para comparar si es mayor o igual
# <= para comparar si es menor o igual


a = 6
b = 4   
print(a == b)  # False, porque 6 no es igual a 4
print(a != b)  # True, porque 6 no es igual a 4
print(a > b)   # True, porque 6 es mayor que 4
print(a < b)   # False, porque 6 no es menor que 4
print(a >= b)  # True, porque 6 es mayor o igual a 4
print(a <= b)  # False, porque 6 no es menor o igual a
