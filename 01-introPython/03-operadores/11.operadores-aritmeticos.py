# Aritméticos: 

# + para sumar
# - para restar 
# * para multiplicar
# / para dividir
# // para dividir y obtener el cociente entero (floor division)
# % para obtener el resto de la división (modulo - modulus)
# ** para elevar un número a una potencia (exponentiation)# Ejemplo de uso de operadores aritméticos

a = 6
b = 4

c = a // b
d = a % b
e = a ** b
print(c)
print(type(c))
print(d)
print(type(d))
print(e)
print(type(e))